public class Ejercicio1bc {
    //Ejercicio 1
    //Escribir los 10 primeros números (ascendente y descendente)
    public static void main(String[] args) {

        System.out.println("Números ascendientes: ");
        for (int i = 1; i <=10 ; i++) {
            System.out.println(i);
        }
        System.out.println("Números descedientes: ");
        for (int j = 10; j >= 1; j--){
            System.out.println(j);

        }




    }
}
